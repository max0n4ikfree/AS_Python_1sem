def SwapCol(A, K1, K2):
    """
    Меняет местами столбцы K1 и K2 в матрице A
    
    Параметры:
    A - матрица (список списков)
    K1 - номер первого столбца (индекс начинается с 0)
    K2 - номер второго столбца (индекс начинается с 0)
    """
    for i in range(len(A)):
        # Меняем элементы в строке i столбцов K1 и K2
        A[i][K1], A[i][K2] = A[i][K2], A[i][K1]
